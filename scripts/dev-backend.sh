#!/usr/bin/env bash
set -euo pipefail

cd "$(dirname "$0")/.."

if [[ ! -d .venv ]]; then
  python3 -m venv .venv
fi

source .venv/bin/activate
pip install -U pip >/dev/null
pip install -r backend/requirements.txt >/dev/null

export PYTHONPATH=backend
uvicorn app.main:app --host 0.0.0.0 --port 8000 --reload
